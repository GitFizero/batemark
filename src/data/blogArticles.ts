export function getArticleBySlug(slug: string): BlogArticleData | undefined {
  return blogArticles.find(a => a.slug === slug);
}

export function getRelatedArticles(slug: string, limit = 3): BlogArticleData[] {
  const article = getArticleBySlug(slug);
  if (!article) return blogArticles.slice(0, limit);
  return article.relatedSlugs
    .map(s => getArticleBySlug(s))
    .filter((a): a is BlogArticleData => !!a)
    .slice(0, limit);
}
